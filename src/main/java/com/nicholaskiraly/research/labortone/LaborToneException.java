package com.nicholaskiraly.research.labortone;

public class LaborToneException extends Exception {

  public LaborToneException() {
    super();
  }

  public LaborToneException(String message) {
    super(message);
  }

  public LaborToneException(String message, Throwable cause) {
    super(message, cause);
  }

  public LaborToneException(Throwable cause) {
    super(cause);
  }
}
